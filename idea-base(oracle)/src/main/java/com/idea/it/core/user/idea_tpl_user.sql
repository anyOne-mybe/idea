CREATE TABLE IDEA_TPL_USER(
	USER_ID        INT(15) NOT NULL AUTO_INCREMENT,
	USER_NAME      VARCHAR(50),
	USER_ACCOUNT   VARCHAR(50),
	PASSWORD       VARCHAR(20),
	EMAIL          VARCHAR(50),
	SEX            CHAR(1),
    AGE            INT(3),
    WEB_CHAT       VARCHAR(20),
    QQ             VARCHAR(18),
    IDENTITY_CARD  VARCHAR(18),
    YEAR           INT(4),
    MONTH          INT(2),
    DAY            INT(2),
	ADDRESS_ID     INT(15),
	AVAILABLE      CHAR(1),
	CREATE_BY      INT(15),
	CREATE_TIME    TIMESTAMP,
	UPDATE_BY      INT(15),
	UPDATE_TIME      TIMESTAMP,
	PRIMARY KEY(USER_ID),
	UNIQUE(USER_ACCOUNT)
)